import { NextRequest, NextResponse } from "next/server";
import { getCalibrationStats, listPredictions } from "@/lib/db/queries";
import { getCurrentProbabilities } from "@/lib/db/probability";
import { getWorkspaceId } from "@/lib/db/workspace";

export const dynamic = "force-dynamic";

/**
 * GET /api/reports/calibration?format=json|markdown
 *
 * Generates a calibration report showing:
 * - Overall prediction accuracy
 * - Accuracy by confidence bucket
 * - Active thesis probabilities with momentum
 * - Resolution rate
 *
 * Use format=markdown for LP-ready reports.
 */
export async function GET(req: NextRequest) {
  const workspaceId = await getWorkspaceId();
  const format = req.nextUrl.searchParams.get("format") ?? "json";

  const [calibration, allPreds, pendingPreds, thesisProbs] = await Promise.all([
    getCalibrationStats(workspaceId),
    listPredictions(workspaceId, "all"),
    listPredictions(workspaceId, "pending"),
    getCurrentProbabilities(workspaceId),
  ]);

  const totalPredictions = allPreds.length;
  const pendingCount = pendingPreds.length;
  const resolvedCount = totalPredictions - pendingCount;
  const resolutionRate =
    totalPredictions > 0
      ? Math.round((resolvedCount / totalPredictions) * 1000) / 10
      : 0;

  const report = {
    generatedAt: new Date().toISOString(),
    summary: {
      totalPredictions,
      resolvedCount,
      pendingCount,
      resolutionRate,
      overallAccuracy: calibration.accuracy,
      correctPredictions: calibration.correct,
    },
    calibrationByConfidence: calibration.byConfidence,
    activeTheses: thesisProbs.map((t) => ({
      title: t.title,
      direction: t.direction,
      domain: t.domain,
      probability: Math.round(t.probability * 1000) / 10,
      momentum: t.momentum !== null ? Math.round(t.momentum * 1000) / 10 : null,
      signalCount: t.signalCount,
      lastComputed: t.computedAt,
    })),
  };

  if (format === "markdown") {
    const md = generateMarkdownReport(report);
    return new NextResponse(md, {
      headers: {
        "Content-Type": "text/markdown; charset=utf-8",
        "Content-Disposition": `attachment; filename="calibration-report-${new Date().toISOString().split("T")[0]}.md"`,
      },
    });
  }

  return NextResponse.json(report);
}

function generateMarkdownReport(report: {
  generatedAt: string;
  summary: {
    totalPredictions: number;
    resolvedCount: number;
    pendingCount: number;
    resolutionRate: number;
    overallAccuracy: number;
    correctPredictions: number;
  };
  calibrationByConfidence: Record<string, { correct: number; total: number; accuracy: number }>;
  activeTheses: Array<{
    title: string;
    direction: string;
    domain: string;
    probability: number;
    momentum: number | null;
    signalCount: number;
    lastComputed: Date | null;
  }>;
}): string {
  const { summary, calibrationByConfidence, activeTheses } = report;

  let md = `# Signal Tracker — Calibration Report\n`;
  md += `**Generated:** ${new Date(report.generatedAt).toLocaleDateString("en-US", { year: "numeric", month: "long", day: "numeric" })}\n\n`;

  md += `## Summary\n\n`;
  md += `| Metric | Value |\n|--------|-------|\n`;
  md += `| Total Predictions | ${summary.totalPredictions} |\n`;
  md += `| Resolved | ${summary.resolvedCount} |\n`;
  md += `| Pending Resolution | ${summary.pendingCount} |\n`;
  md += `| Resolution Rate | ${summary.resolutionRate}% |\n`;
  md += `| Overall Accuracy | **${summary.overallAccuracy}%** (${summary.correctPredictions}/${summary.resolvedCount}) |\n\n`;

  md += `## Calibration by Confidence Bucket\n\n`;
  md += `| Confidence Range | Predictions | Correct | Accuracy |\n`;
  md += `|-----------------|-------------|---------|----------|\n`;
  for (const [bucket, stats] of Object.entries(calibrationByConfidence)) {
    md += `| ${bucket}% | ${stats.total} | ${stats.correct} | ${stats.accuracy}% |\n`;
  }
  md += `\n`;

  if (activeTheses.length > 0) {
    md += `## Active Theses\n\n`;
    md += `| Thesis | Direction | Probability | Momentum | Signals |\n`;
    md += `|--------|-----------|-------------|----------|--------|\n`;
    for (const t of activeTheses) {
      const momentumStr =
        t.momentum !== null
          ? `${t.momentum > 0 ? "+" : ""}${t.momentum}%`
          : "—";
      md += `| ${t.title} | ${t.direction.toUpperCase()} | ${t.probability}% | ${momentumStr} | ${t.signalCount} |\n`;
    }
    md += `\n`;
  }

  md += `---\n*Report generated by SignalTracker*\n`;
  return md;
}
